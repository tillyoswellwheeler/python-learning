# Using variables and numbers

my_num = 5
print(str(my_num)) # Changes the int to a str
print(abs(my_num)) # Absolute value it counts for
print(pow(3,2)) # pow() is first argument to the second argument
print(max(9, 10)) # This outputs the largest number
print(min(3, 11)) # This outputs the smallest number
print(round(3.32)) # This rounds a float to the nearest int

#------------------------------------
# Using the Math module

this_is_ceil = ceil(3.7) # This ceil always rounds the number up
this_is_floor = floor(3.6) # This floor always rounds the number down
this_is_squareroot = sqrt(9) # This returns the squareroot of a number as a float

print(this_is_ceil)
print(this_is_floor)
print(this_is_squareroot)

